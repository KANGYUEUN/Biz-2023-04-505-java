package com.callor.hello.data;

	public class VarA {
		public static void main(String[] args) {
			/*
			 * 기억장소 어딘가에 "정수(int, 왜 150이 정수임)"
			 * 저장할수 있는 공간을 한개 예약하고 num1 이름을 붙임 (label)
			 * 이를 변수 num1 이라 부른다.
			 * 그리고, 변수 num1에 정수 150을 보관해라  
			 */
			var num1 = 150;
			var num2 = 50;
			System.out.println(num1 + num2);
			System.out.println(num1 - num2);
			System.out.println(num1 * num2);
			System.out.println(num1 / num2);
			System.out.println(num1 % num2);
			
			
		}
}
