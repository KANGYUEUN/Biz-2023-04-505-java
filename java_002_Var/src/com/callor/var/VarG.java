package com.callor.var;

public class VarG {
	 public static void main(String[] args) {
		int num1 = 0;
		
		num1 = num1 + 10; // 10
		num1 += 10;
				
		num1 = num1 - 5; // 5
		num1 -= 5;
		
		num1 = num1 * 3; // 15
		num1 *= 3;
		num1 = num1 / 2; // 7 
		num1 /= 2;
		num1 = num1 % 2; // 1
		num1 %= 2;
		
		
	}
}
