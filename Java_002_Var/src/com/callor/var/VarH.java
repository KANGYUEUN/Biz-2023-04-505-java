package com.callor.var;

public class VarH {
	public static void main(String[] args) {
		
		int num = 0;
		num = num + 1;
		num += 1;
		num ++;  // num 변수에 저장된 값을 1증가
		
		num = num - 1;
		num -= 1;
		num --; // num 변수에 저장된 값을 1감소
		
		
		// 1 증가 시키는 것을 5번 반복
		num++;
		num++;
		num++;
		num++;
		num++;
		
		
		
		
		
	}
}
